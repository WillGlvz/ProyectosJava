    void Suma() {
        String cad1, cad2;
        int a, b, s;
        
        cad1 = txtnum1.getText();
        cad2 = txtnum2.getText();
        a = Integer.parseInt(cad1);
        b = Integer.parseInt(cad2);
        s = a + b;
        lblresultado.setText("" + s);
    }
    
    void Resta() {
        String cad1, cad2;
        int a, b, r;
        
        cad1 = txtnum1.getText();
        cad2 = txtnum2.getText();
        a = Integer.parseInt(cad1);
        b = Integer.parseInt(cad2);
        r = a - b;
        lblresultado.setText("" + r);
    }    
    
    void Borrar() {
        txtnum1.setText("");
        txtnum2.setText("");
    }

