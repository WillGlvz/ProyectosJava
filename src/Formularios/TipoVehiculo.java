    private void BtnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnAgregarActionPerformed
        if("".equals(txtnombre.getText().trim()) || "".equals(txtdescripcion.getText().trim()) || SliderPorcentaje.getValue() == 0){
            JOptionPane.showMessageDialog(this, "No puedes dejar campos en blanco", "AVISO", JOptionPane.CANCEL_OPTION);
        }
        else{
            try {
                PreparedStatement st = cn.prepareStatement("SELECT nombre FROM tipo_vehiculo WHERE nombre = ?");
                st.setString(1, txtnombre.getText());
                ResultSet rs = st.executeQuery();
                if(rs.next()){
                    JOptionPane.showMessageDialog(this, "Ese tipo de uso ya existe", "AVISO", JOptionPane.CANCEL_OPTION);
                }
                else{
                    st = cn.prepareStatement("INSERT INTO tipo_vehiculo(nombre, porcentaje, descripcion) VALUES(?, ?, ?)");
                    st.setString(1, txtnombre.getText());
                    st.setInt(2, SliderPorcentaje.getValue());
                    st.setString(3, txtdescripcion.getText());
                    st.executeUpdate();
                    JOptionPane.showMessageDialog(this, "Datos agregados", "AVISO", JOptionPane.INFORMATION_MESSAGE);
                    MostrarDatos();
                    limpiar();
                }
            } catch (HeadlessException | SQLException e) {
                JOptionPane.showMessageDialog(this, "ERROR: " + e, "ATENCION", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_BtnAgregarActionPerformed

