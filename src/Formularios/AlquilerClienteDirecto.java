    public void CargarClientes() 
    {
        try {
            Statement st = cn.createStatement();
            ResultSet rs = st.executeQuery("SELECT apellidos + ', ' + nombres AS nombres FROM cliente_directo ORDER BY apellidos");
            cmbCliente.removeAllItems();
            while(rs.next())
            {
                cmbCliente.addItem(rs.getString(1));
            }
        } catch (SQLException ex) {
            Logger.getLogger(AlquilerClienteDirecto.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    public void CargarVehiculos() 
    {
        try {
            Statement st = cn.createStatement();
            ResultSet rs = st.executeQuery("SELECT marca + ', ' + modelo AS modelo FROM vehiculos ORDER BY marca");
            cmbvehiculos.removeAllItems();
            while(rs.next())
            {
                cmbvehiculos.addItem(rs.getString(1));
            }
        } catch (SQLException ex) {
            Logger.getLogger(AlquilerClienteDirecto.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
