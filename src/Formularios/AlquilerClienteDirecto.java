    private void BtnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnAgregarActionPerformed
        if (cmbvehiculos.getSelectedIndex() >= 0) {
            if (TablaOrden.getRowCount() == 0) {
                DefaultTableModel modelo = (DefaultTableModel) TablaOrden.getModel();
                Object[] etiquetas = {"Vehiculo", "Precio"};
                modelo.setColumnIdentifiers(etiquetas);
                Object [] fila = new Object[2];
                fila[0] = cmbvehiculos.getSelectedItem();
                fila[1] = txtprecio.getText();       
                modelo.addRow(fila); 
                TablaOrden.setModel(modelo);
                cmbvehiculos.setSelectedIndex(-1);
            }
            else {
                boolean denegar = false;
                for (int i = 0; i < TablaOrden.getRowCount(); i++) {
                    if (TablaOrden.getValueAt(i, 0).toString().equals(cmbvehiculos.getSelectedItem().toString())) {
                        denegar = true;
                        i = TablaOrden.getRowCount();
                    }
                }
                if (denegar) {
                    JOptionPane.showMessageDialog(this, "¡Ya agregó este vehiculo!", "AVISO", JOptionPane.CANCEL_OPTION);
                    cmbvehiculos.setSelectedIndex(-1);
                }
                else {
                    DefaultTableModel modelo = (DefaultTableModel) TablaOrden.getModel();
                    Object [] fila = new Object[2];
                    fila[0] = cmbvehiculos.getSelectedItem();
                    fila[1] = txtprecio.getText();
                    modelo.addRow(fila);
                    TablaOrden.setModel(modelo);
                    cmbvehiculos.setSelectedIndex(-1);
                }
            }
        }
        else {
            JOptionPane.showMessageDialog(this, "¡Debe seleccionar un vehiculo!", "AVISO", JOptionPane.CANCEL_OPTION);
        }
    }//GEN-LAST:event_BtnAgregarActionPerformed

