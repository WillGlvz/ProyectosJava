    private void BrnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BrnAgregarActionPerformed
        if(txtnombre.getText().isEmpty() || txtnit.getText().isEmpty() || txtnrc.getText().isEmpty() || txttelelfono.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "No puedes dejar campos en blanco", "AVISO", JOptionPane.CANCEL_OPTION);
        }
        else if (!ClsValidaciones.ComprobarNumeroTelefono(txttelelfono.getText())) {
            JOptionPane.showMessageDialog(this, "Numero de telefono incorrecto", "AVISO", JOptionPane.CANCEL_OPTION);
            txttelelfono.setText(null);
        }
        else{
            try {
                PreparedStatement st = cn.prepareStatement("SELECT nombre FROM agencia_viaje WHERE nombre = ?");
                st.setString(1, txtnombre.getText());
                ResultSet rs = st.executeQuery();
                if(rs.next()){
                    JOptionPane.showMessageDialog(this, "Este nombre de agencia ya existe", "AVISO", JOptionPane.CANCEL_OPTION);
                }
                else{
                    st = cn.prepareStatement("INSERT INTO agencia_viaje VALUES (?, ?, ?, ?, ?)");
                    st.setString(1, txtnombre.getText());
                    st.setString(2, txtnit.getText());
                    st.setString(3, txtnrc.getText());
                    st.setString(4, txttelelfono.getText());
                    st.setString(5, txtdireccion.getText());
                    st.executeUpdate();
                    JOptionPane.showMessageDialog(this, "Datos agregados", "AVISO", JOptionPane.INFORMATION_MESSAGE);
                    MostrarDatos();
                    limpiar();
                }
            } catch (SQLException | HeadlessException e) {
                System.out.println(e);
            }
        }
    }//GEN-LAST:event_BrnAgregarActionPerformed

    private void BtnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnModificarActionPerformed
        if(txtnombre.getText().isEmpty() || txtnit.getText().isEmpty() || txtnrc.getText().isEmpty() || txttelelfono.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "No puedes dejar campos en blanco", "AVISO", JOptionPane.CANCEL_OPTION);
        }
        else if (!ClsValidaciones.ComprobarNumeroTelefono(txttelelfono.getText())) {
            JOptionPane.showMessageDialog(this, "Numero de telefono incorrecto", "AVISO", JOptionPane.CANCEL_OPTION);
            txttelelfono.setText(null);
        }
        else{
            try {
                PreparedStatement st = cn.prepareStatement("SELECT nombre FROM agencia_viaje WHERE nombre = ? AND NOT codigo_agencia = ?");
                st.setString(1, txtnombre.getText());
                st.setInt(2, Integer.parseInt(txtcodigo.getText()));
                ResultSet rs = st.executeQuery();
                if(rs.next()){
                    JOptionPane.showMessageDialog(this, "Ya existe esta agencia", "AVISO", JOptionPane.CANCEL_OPTION);
                }
                else{
                    st = cn.prepareStatement("UPDATE agencia_viaje SET nombre = ?, nit = ?, nrc = ?, telefono = ?, direccion = ? WHERE codigo_agencia = ?");
                    st.setString(1, txtnombre.getText());
                    st.setString(2, txtnit.getText());
                    st.setString(3, txtnrc.getText());
                    st.setString(4, txttelelfono.getText());
                    st.setString(5, txtdireccion.getText());
                    st.setInt(6, Integer.parseInt(txtcodigo.getText()));
                    st.executeUpdate();
                    JOptionPane.showMessageDialog(this, "Datos modificados", "AVISO", JOptionPane.INFORMATION_MESSAGE);
                    MostrarDatos();
                    limpiar();
                }
            } catch (SQLException | NumberFormatException | HeadlessException e) {
                System.out.println(e);
            }
        }
    }//GEN-LAST:event_BtnModificarActionPerformed

