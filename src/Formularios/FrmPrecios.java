    private void BtnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnModificarActionPerformed
        if(txttipo.getText().isEmpty() || txtprecio.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "No puedes dejar campos en blanco", "AVISO", JOptionPane.CANCEL_OPTION);
        }
        else if(Double.parseDouble(txtprecio.getText()) > 10.00){
            JOptionPane.showMessageDialog(this, "El precio no puede ser mayor a 10", "AVISO", JOptionPane.CANCEL_OPTION);
        }
        else if(Double.parseDouble(txtprecio.getText()) == 0.00){
            JOptionPane.showMessageDialog(this, "El precio debe ser mayor a 0", "AVISO", JOptionPane.CANCEL_OPTION);
        }
        else if(lblvalor.getText() == null){
            JOptionPane.showMessageDialog(this, "Debes establecer un valor", "AVISO", JOptionPane.CANCEL_OPTION);
        }
        else{
            try {
                PreparedStatement st = cn.prepareStatement("SELECT nombre_tipo FROM tipo_vehiculos WHERE nombre_tipo = ? AND NOT codigo_tipo = ?");
                st.setString(1, txttipo.getText());
                st.setInt(2, Integer.parseInt(txtcodigo.getText()));
                ResultSet rs = st.executeQuery();
                if(rs.next()){
                    JOptionPane.showMessageDialog(this, "Ya existe este usuario", "AVISO", JOptionPane.CANCEL_OPTION);
                }
                else{
                    st = cn.prepareStatement("UPDATE tipo_vehiculos SET tarifa_tipo = ?, porcentaje = ? WHERE codigo_tipo = ?");
                    st.setDouble(1, Double.parseDouble(txtprecio.getText()));
                    st.setInt(2, SliderPorcentaje.getValue());
                    st.setInt(3, Integer.parseInt(txtcodigo.getText()));
                    st.executeUpdate();
                    JOptionPane.showMessageDialog(this, "Datos modificados", "AVISO", JOptionPane.INFORMATION_MESSAGE);
                    MostrarDatos();
                    limpiar();
                }
            } catch (HeadlessException | NumberFormatException | SQLException e) {
                System.out.println(e);
            }
        }
    }//GEN-LAST:event_BtnModificarActionPerformed

