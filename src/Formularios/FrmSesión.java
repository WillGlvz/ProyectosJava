    int intentos = 0;
    void MtoIntentosFallidos()
        {
            intentos = intentos + 1;
            if (intentos < 3)
            {
                ClsSonidos.IntentosFallidos();
                JOptionPane.showMessageDialog(this, "¡Datos incorrectos!", "ERROR", JOptionPane.CANCEL_OPTION);
                txtUsuario.setText(null);
                txtContraseña.setText(null);
                txtUsuario.requestFocusInWindow();
            }
            else
            {
                ClsSonidos.IntentosFallidos();
                JOptionPane.showMessageDialog(this, "¡Intentos fallidos!", "ERROR", JOptionPane.ERROR_MESSAGE);
                System.exit(0);
            }
        }
    
    private void obtenerDatos(){
        hora = calendar.get(Calendar.HOUR_OF_DAY);
        minuto = calendar.get(Calendar.MINUTE);
        segundo = calendar.get(Calendar.SECOND);
    }
    private void mostrarDatos(){
        lblhora.setText(hora + " : " + minuto + " : " + segundo);
    }
    
    private void validarHora(){
        if(segundo == 59){
            minuto++;
            segundo = 0;
            if(minuto == 59){
                hora++;
                minuto = 0;
                if(hora == 23){
                    hora = 0;
                }
            }
        }else{
                segundo++;
            }
        }
