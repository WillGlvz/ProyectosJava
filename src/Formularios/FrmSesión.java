    private void BtnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnAceptarActionPerformed
        if(txtUsuario.getText().isEmpty() || txtContraseña.getText().isEmpty()){
            MtoIntentosFallidos();
        }
        else{
            try {
                PreparedStatement st = cn.prepareStatement("SELECT codigo_usuario, nombres, apellidos, usuario, codigo_tipo_usuario FROM usuarios WHERE usuario = ? AND contraseña = CONVERT(VARCHAR(32), HashBytes('MD5', ?), 2)");
                st.setString(1, txtUsuario.getText());
                st.setString(2, txtContraseña.getText());
                ResultSet rs = st.executeQuery();
                if(rs.next()){
                    ClsSQLQuery.ProCodigoUsuario = rs.getInt(1);
                    ClsSQLQuery.ProNombresUsuario = rs.getString(2);
                    ClsSQLQuery.ProApellidosUsuario = rs.getString(3);
                    ClsSQLQuery.ProNombreUsuario = rs.getString(4);
                    ClsSQLQuery.ProTipoUsuario = rs.getInt(5);
                    JOptionPane.showMessageDialog(this, "Bienvenid@ " + ClsSQLQuery.ProNombreUsuario, "Welcome", JOptionPane.INFORMATION_MESSAGE);
                    st = cn.prepareStatement("INSERT INTO historial(usuario, hora, fecha) VALUES(?, ?, ?)");
                    st.setString(1, ClsSQLQuery.ProNombreUsuario);
                    st.setString(2, lblhora.getText());
                    st.setString(3, lblfecha.getText());
                    st.executeUpdate();
                    ClsSonidos.Sesion();
                    FrmPrincipal obj = new FrmPrincipal();
                    obj.setVisible(true);
                    dispose();
                }
                else{
                    MtoIntentosFallidos();
                }
            } catch (SQLException | HeadlessException e) {
                System.out.println(e);
            }
        }
    }//GEN-LAST:event_BtnAceptarActionPerformed

