    private void BtnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnModificarActionPerformed
        if(txtnombre.getText().isEmpty() || txtnit.getText().isEmpty() || txttelefono.getText().isEmpty() || txtdireccion.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "No puedes dejar campos en blanco", "AVISO", JOptionPane.CANCEL_OPTION);
        }
        else if (!ClsValidaciones.ComprobarNumeroTelefono(txttelefono.getText())) {
            JOptionPane.showMessageDialog(this, "Numero telefonico incorrecto", "AVISO", JOptionPane.CANCEL_OPTION);
            txttelefono.setText(null);
            txttelefono.requestFocusInWindow();
        }
        else{
            try {
                PreparedStatement st = cn.prepareStatement("UPDATE DatosParqueo SET Nombre = ?, NIT = ?, Direccion = ?, Telefono = ?");
                st.setString(1, txtnombre.getText());
                st.setString(2, txtnit.getText());
                st.setString(3, txtdireccion.getText());
                st.setString(4, txttelefono.getText());
                st.executeUpdate();
                JOptionPane.showMessageDialog(this, "Datos modificados", "AVISO", JOptionPane.INFORMATION_MESSAGE);
                MostrarDatos();
                limpiar();
            } catch (HeadlessException | SQLException e) {
                System.out.println(e);
            }
        }
    }//GEN-LAST:event_BtnModificarActionPerformed

