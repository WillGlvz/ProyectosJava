    private void BtnEnviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnEnviarActionPerformed
        if ("".equals(txtusuario.getText().trim()) || "".equals(txtcorreo.getText().trim())) {
            JOptionPane.showMessageDialog(this, "¡Datos incorrectos!", "ERROR", JOptionPane.ERROR_MESSAGE);
            txtusuario.setText(null);
            txtcorreo.setText(null);
            txtusuario.requestFocusInWindow();
        }
        else{
            try {
            PreparedStatement st = cn.prepareStatement("SELECT codigo_usuario FROM usuarios WHERE usuario = ? AND correo = ?");
            st.setString(1, txtusuario.getText());
            st.setString(2, txtcorreo.getText());
            ResultSet rs = st.executeQuery();
            if(rs.next())
            {
                int codigo_usuario = rs.getInt(1);
                String Contraseña = mthGenerarContraseña();
                st = cn.prepareStatement("UPDATE usuarios SET contraseña = CONVERT(VARCHAR(32), HashBytes('MD5', ?), 2) WHERE codigo_usuario = ?");
                st.setString(1, Contraseña);
                st.setInt(2, codigo_usuario);
                st.executeUpdate();
                ClsCorreo.mthEnviar(txtcorreo.getText(), "Su nueva contraseña es: " + Contraseña + "\n\nEasyParking System");
                JOptionPane.showMessageDialog(this, "¡Se ha enviado una contraseña nueva a su correo!", "AVISO", JOptionPane.INFORMATION_MESSAGE);
                dispose();
            }
            else {
                    JOptionPane.showMessageDialog(this, "¡Datos incorrectos!", "ERROR", JOptionPane.ERROR_MESSAGE);
                    txtusuario.setText(null);
                    txtcorreo.setText(null);
                    txtusuario.requestFocusInWindow();
                }
            }
            catch (SQLException | NumberFormatException e) {
                JOptionPane.showMessageDialog(this, e.getMessage());
            }
        }
    }//GEN-LAST:event_BtnEnviarActionPerformed

