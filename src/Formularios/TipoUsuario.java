    void MostrarDatos()
    {
    modelo= new DefaultTableModel();
    modelo.addColumn("Codigo");
    modelo.addColumn("Nombre");
    modelo.addColumn("Descripcion");
    modelo.addColumn("Usuarios");
    modelo.addColumn("Tipo usuarios");
    modelo.addColumn("Flota");
    modelo.addColumn("Clientes");
    modelo.addColumn("Alquileres");
    modelo.addColumn("Reportes");
    modelo.addColumn("Contaduria");
    Tabla.setModel(modelo);
    String sql="SELECT * FROM tipo_usuario";
    String []datos = new String [10];
        try {
            Statement st = cn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            while(rs.next()){
                datos[0]=rs.getString(1);
                datos[1]=rs.getString(2);
                datos[2]=rs.getString(3);
                datos[3]=rs.getString(4);
                datos[4]=rs.getString(5);
                datos[5]=rs.getString(6);
                datos[6]=rs.getString(7);
                datos[7]=rs.getString(8);
                datos[8]=rs.getString(9);
                datos[9]=rs.getString(10);
                modelo.addRow(datos);
            }
            Tabla.setModel(modelo);
        } catch (SQLException ex) {
            Logger.getLogger(TipoUsuario.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
