    private void BtnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnModificarActionPerformed
        if(txtnombre.getText().isEmpty() || txtdescripcion.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "No puedes dejar campos en blanco", "AVISO", JOptionPane.CANCEL_OPTION);
        }
        else if(!(Chk_usuarios.isSelected() || Chk_reportes.isSelected() || Chk_aquiler.isSelected() || Chk_clientes.isSelected() || Chk_conta.isSelected() || Chk_flota.isSelected() || Chk_tipo.isSelected())){
            JOptionPane.showMessageDialog(this, "¬°Debe seleccionar al menos un permiso para crear un tipo de usuario!", "AVISO", JOptionPane.CANCEL_OPTION);
        }
        else if("".equals(txtcodigo.getText())){
            JOptionPane.showMessageDialog(this, "Debes seleccionar un registro para modificar", "AVISO", JOptionPane.CANCEL_OPTION);
        }
        else{
            try {
                PreparedStatement st = cn.prepareStatement("SELECT nombre FROM tipo_usuario WHERE nombre = ? AND NOT codigo_tipo_usuario = ?");
                st.setString(1, txtnombre.getText());
                st.setInt(2, Integer.parseInt(txtcodigo.getText()));
                ResultSet rs = st.executeQuery();
                if(rs.next()){
                    JOptionPane.showMessageDialog(this, "Ya existe este tipo de usuario", "AVISO", JOptionPane.CANCEL_OPTION);
                }
                else{
                    st = cn.prepareStatement("UPDATE tipo_usuario SET nombre_tipo = ?, descripcion = ?, usuario = ?, tipo_usuario = ?, flota = ?, clientes = ?, alquileres = ?, reportes = ?, contaduria = ? WHERE codigo_tipo_usuario = ?");
                    st.setString(1, txtnombre.getText());
                    st.setString(2, txtdescripcion.getText());
                    st.setBoolean(3, Chk_usuarios.isSelected());
                    st.setBoolean(4, Chk_tipo.isSelected());
                    st.setBoolean(5, Chk_flota.isSelected());
                    st.setBoolean(6, Chk_clientes.isSelected());
                    st.setBoolean(7, Chk_aquiler.isSelected());
                    st.setBoolean(8, Chk_reportes.isSelected());
                    st.setBoolean(9, Chk_conta.isSelected());
                    st.setInt(10, Integer.parseInt(txtcodigo.getText()));
                    st.executeUpdate();
                    JOptionPane.showMessageDialog(this, "Datos modificados", "AVISO", JOptionPane.INFORMATION_MESSAGE);
                    MostrarDatos();
                    Limpiar();
                }
            } catch (Exception e) {
            }
        }
    }//GEN-LAST:event_BtnModificarActionPerformed

