    private void BtnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnEliminarActionPerformed
        if("".equals(txtcodigo.getText())) {
            JOptionPane.showMessageDialog(this, "¬°Debe realizar una consulta para eliminar!", "AVISO", JOptionPane.CANCEL_OPTION);
        }
        else if("1".equals(txtcodigo.getText())){
            JOptionPane.showMessageDialog(this, "No puedes eliminar al administrador del sistema", "AVISO", JOptionPane.CANCEL_OPTION);
        }
        else{
            try{
            PreparedStatement st = cn.prepareStatement("DELETE FROM tipo_usuario WHERE codigo_tipo_usuario = ?");
            st.setInt(1, Integer.parseInt(txtcodigo.getText()));
            int eliminar = JOptionPane.showConfirmDialog(this, "¬øEst√° seguro que desea eliminar?", "Atenci√≥n", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
            if(eliminar == 0){
                st.execute();
                    int filaselecionada= Tabla.getSelectedRow();
                    if(filaselecionada>=0){
                    modelo.removeRow(filaselecionada);
                    JOptionPane.showMessageDialog(this, "Datos eliminados");
                    Limpiar();
                    }
                    else
                    {
                        JOptionPane.showMessageDialog(this, "Error al eliminar");
                    }
                }
            }catch(SQLException | NumberFormatException | HeadlessException e){
                System.out.println(e);
            }
        }
    }//GEN-LAST:event_BtnEliminarActionPerformed

