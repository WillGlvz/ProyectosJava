    private void BtnIniciarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnIniciarActionPerformed
        if(cmbvehiculo.getSelectedIndex() == -1){
            JOptionPane.showMessageDialog(this, "Debes seleccionar un vehiculo", "AVISO", JOptionPane.CANCEL_OPTION);
        }
        else if(!(Chk_aceite.isSelected() || Chk_agua.isSelected() || Chk_direccion.isSelected() || Chk_frenos.isSelected() || Chk_neumaticos.isSelected() || Chk_transm.isSelected() || Chk_tapiceria.isSelected() || Chk_suspension.isSelected() || Chk_sisElect.isSelected() || Chk_pintura.isSelected())){
            JOptionPane.showMessageDialog(this, "¡Debe seleccionar al menos un tipo de revision!", "AVISO", JOptionPane.CANCEL_OPTION);
        }
        else{
            Act = new ActionListener() {
                @Override
                public void actionPerformed(ActionEvent ae) {
                    if (ProgressBar.getValue() < 100) {
                        ProgressBar.setValue(ProgressBar.getValue()+1);
                    }
                    else {
                        t.stop();
                        cerrar();
                    }
                }
            };
            t = new Timer(15, Act);
            t.start();
        }
    }//GEN-LAST:event_BtnIniciarActionPerformed

