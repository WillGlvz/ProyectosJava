    public void CargarDatos() 
    {
        try {
            Statement st = cn.createStatement();
            ResultSet rs = st.executeQuery("SELECT (cast(codigo_vehiculo AS varchar(50)) + ' - ' + marca + ', ' + modelo) AS nombre FROM vehiculos ORDER BY marca");
            cmbvehiculo.removeAllItems();
            while(rs.next())
            {
                cmbvehiculo.addItem(rs.getString(1));
            }
        } catch (SQLException ex) {
            Logger.getLogger(Vehiculos.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    public void cerrar(){
        JOptionPane.showMessageDialog(this, "Revision finalizada con exito", "AVISO", JOptionPane.INFORMATION_MESSAGE);
        lblestado.setText("Revision finalizada");
        estado = this.lblestado.getText();
        this.dispose();
    }
