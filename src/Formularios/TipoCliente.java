    private void BtnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnAceptarActionPerformed
        if(cmbtipo.getSelectedIndex() == -1){
            JOptionPane.showMessageDialog(this, "Debes seleccionar un tipo de cliente", "AVISO", JOptionPane.CANCEL_OPTION);
        }
        else if(cmbtipo.getSelectedIndex() == 0){
            ClienteDirecto obj = new ClienteDirecto(null, true);
            hide();
            obj.setVisible(true);
        }
        else if(cmbtipo.getSelectedIndex() == 1){
            AgenciaViaje obj = new AgenciaViaje(null, true);
            hide();
            obj.setVisible(true);
        }
    }//GEN-LAST:event_BtnAceptarActionPerformed

