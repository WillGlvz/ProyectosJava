    private void BtnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnAgregarActionPerformed
        if(cmbestado.getSelectedIndex() == -1){
            JOptionPane.showMessageDialog(this, "Debe establecer el estado del puesto", "AVISO", JOptionPane.CANCEL_OPTION);
        }
        else{
        try {
            if(cmbestado.getSelectedItem().equals("Libre")){
            PreparedStatement st = cn.prepareStatement("INSERT INTO puesto VALUES(?, ?, 1)");
            st.setInt(1, Integer.parseInt(txtcodigo.getText()));
            st.setString(2, cmbestado.getSelectedItem().toString());
            st.executeUpdate();
            JOptionPane.showMessageDialog(this, "Puesto agregado", "AVISO", JOptionPane.INFORMATION_MESSAGE);
            MostrarDatos();
            txtcodigo.setText(null);
            cmbestado.setSelectedIndex(-1);
            }
            else if(cmbestado.getSelectedItem().equals("Reservado")){
                PreparedStatement st = cn.prepareStatement("INSERT INTO puesto VALUES(?, ?, 2)");
                st.setInt(1, Integer.parseInt(txtcodigo.getText()));
                st.setString(2, cmbestado.getSelectedItem().toString());
                st.executeUpdate();
                JOptionPane.showMessageDialog(this, "Puesto agregado", "AVISO", JOptionPane.INFORMATION_MESSAGE);
                MostrarDatos();
                txtcodigo.setText(null);
                cmbestado.setSelectedIndex(-1);
            }
        } catch (NumberFormatException | SQLException e) {
            JOptionPane.showMessageDialog(this, "Solo puedes agregar puestos de forma ascendente", "AVISO", JOptionPane.CANCEL_OPTION);
            System.out.println(e);
        }
        }
    }//GEN-LAST:event_BtnAgregarActionPerformed

