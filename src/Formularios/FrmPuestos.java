    private void BtnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnEliminarActionPerformed
        if("".equals(txtcodigo.getText())) {
            JOptionPane.showMessageDialog(this, "¡Debe realizar una consulta para eliminar!", "AVISO", JOptionPane.CANCEL_OPTION);
        }
        else{
            try{
            PreparedStatement st = cn.prepareStatement("SELECT estado2 FROM puesto WHERE puesto = ? AND estado2 = 0");
            st.setInt(1, Integer.parseInt(txtcodigo.getText()));
            ResultSet rs6 = st.executeQuery();
            if(rs6.next()){
                JOptionPane.showMessageDialog(this, "No puedes eliminar este puesto", "AVISO", JOptionPane.CANCEL_OPTION);
            }
            else{
            st = cn.prepareStatement("SELECT MAX(puesto) FROM puesto");
            ResultSet rs = st.executeQuery();
            rs.next();
            String Puesto = rs.getString(1);
            st = cn.prepareStatement("DELETE FROM puesto WHERE puesto = ?");
            st.setInt(1, Integer.parseInt(txtcodigo.getText()));
            if(Integer.parseInt(txtcodigo.getText()) < Integer.parseInt(Puesto)){
                JOptionPane.showMessageDialog(this, "Solo puedes eliminar los puestos de forma descendente", "AVISO", JOptionPane.CANCEL_OPTION);
            }
            else{
            int eliminar = JOptionPane.showConfirmDialog(this, "¿Está seguro que desea eliminar?", "Atención", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
            if(eliminar == 0){
                st.execute();
                JOptionPane.showMessageDialog(this, "Datos eliminados");
                MostrarDatos();
                txtcodigo.setText(null);
                cmbestado.setSelectedIndex(-1);
            }
            else
            {
                JOptionPane.showMessageDialog(this, "Error al eliminar");
            }
            }
            }
            }catch(SQLException | NumberFormatException | HeadlessException e){
                System.out.println(e);
            }
        }
        
    }//GEN-LAST:event_BtnEliminarActionPerformed

