    private void cmbusuariosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbusuariosActionPerformed
        if (cmbusuarios.getSelectedIndex() >= 0) {
            ClsSQLQuery obj = new ClsSQLQuery();
            obj.setCodigo_usuario(((ClsID) cmbusuarios.getSelectedItem()).getID());
            if(obj.ConsultarUsuario()){
                if(obj.getFoto().equals("null")){
                    String Ubicacion;
                    @SuppressWarnings("UnusedAssignment")
                    BufferedImage Imagen = null;
                    try {
                        Ubicacion = (new File("ImagenUsuario")).getAbsolutePath() + "\\Anonimo.jpg";
                        Imagen = ImageIO.read(new File(Ubicacion));
                        Image ImagenRedimensionada;
                        ImagenRedimensionada = Imagen.getScaledInstance(lblPicture.getWidth(), lblPicture.getHeight(), Image.SCALE_SMOOTH);
                        ImageIcon ImagenFinal = new ImageIcon(ImagenRedimensionada);
                        lblPicture.setIcon(ImagenFinal);
                    } catch (IOException e) {
                    }
                }
                else{
                    @SuppressWarnings("UnusedAssignment")
                    BufferedImage Imagen = null;
                    try {
                        Imagen = ImageIO.read(new File(new File("ImagenUsuario").getAbsolutePath() + obj.getFoto()));
                        Image ImagenRedimensionada;
                        ImagenRedimensionada = Imagen.getScaledInstance(lblPicture.getWidth(), lblPicture.getHeight(), Image.SCALE_SMOOTH);
                        ImageIcon ImagenFinal = new ImageIcon(ImagenRedimensionada);
                        lblPicture.setIcon(ImagenFinal);
                    } catch (IOException e) {
                        System.out.println();
                    }
                }
            }
        }
    }//GEN-LAST:event_cmbusuariosActionPerformed

