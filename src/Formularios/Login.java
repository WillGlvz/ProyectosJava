    private void BtnEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnEntrarActionPerformed
        if("".equals(txtcontraseña.getText().trim())){
            MtoIntentosFallidos();
        }
        else if(cmbusuarios.getSelectedIndex() == -1){
            MtoIntentosFallidos();
        }
        else{
            try {
                PreparedStatement st = cn.prepareStatement("SELECT codigo_usuario, usuariox FROM usuarios WHERE usuariox = ? AND contraseña = ?");
                st.setString(1, cmbusuarios.getSelectedItem().toString());
                st.setString(2, ClsEncriptar.MtoSHA256(txtcontraseña.getText()));
                ResultSet rs = st.executeQuery();
                if(rs.next()){
                    ClsSQLQuery.ProCodigoUsuario = rs.getInt(1);
                    ClsSQLQuery.ProNombreUsuario = rs.getString(2);
                    JOptionPane.showMessageDialog(this, "Bienvenid@ " + ClsSQLQuery.ProNombreUsuario, "Ola k ace", JOptionPane.INFORMATION_MESSAGE);
                    MenuPrincipal obj = new MenuPrincipal();
                    obj.setVisible(true);
                    dispose();
                }
                else{
                    MtoIntentosFallidos();
                }
            } catch (HeadlessException | SQLException e) {
            }
        }
    }//GEN-LAST:event_BtnEntrarActionPerformed

