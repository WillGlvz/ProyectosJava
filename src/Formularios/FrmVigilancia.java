    /**
     * Creates new form FrmVigilancia
     * @param parent
     * @param modal
     */
    public FrmVigilancia(java.awt.Frame parent, boolean modal) {
        super(parent, modal);
        timer = new Timer(30, new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent ae) {
                refrescarCamara();
            }
        });
        try {
            cam = Webcam.getDefault();
            cam.open();
            timer.start();
        } catch (WebcamException ex) {
            JOptionPane.showMessageDialog(null, "No se puede acceder a la c√°mara en este momento...", "AVISO", JOptionPane.CANCEL_OPTION);
            this.dispose();
        }
        initComponents();
        setLocationRelativeTo(null);
        setTitle("Camara de vigilancia");
        getContentPane().setBackground(Color.darkGray);
    }
    
    public ImageIcon getImagen() {
        setVisible(true);
        return imagen;
    }
    
    private void refrescarCamara() {
        BufferedImage image = cam.getImage();
        if(!(image == null)){
        Image img = image.getScaledInstance(650, 357, Image.SCALE_SMOOTH);
        ImageIcon icon = new ImageIcon(img);
        lblCam.setIcon(icon);
        }
        else{
            timer.stop();
            JOptionPane.showMessageDialog(this, "Camara de vigilancia apagada", "AVISO", JOptionPane.INFORMATION_MESSAGE);
        }
    }

